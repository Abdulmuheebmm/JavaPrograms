package test_yantra;

public class P7 {
public static void main(String[] args) {

	
	
	double a=20.0d;
	float res=(float)a;
	System.out.println("DOUBLE TO FLOAT  "+res);
	
	
	
	long res1=(long)a;
	System.out.println("DOUBLE TO LONG   "+res1);
	
	
	int res2=(int)a;
	System.out.println("DOUBLE TO INT  "+res2);
	
	
	short res3=(short)a;
	System.out.println("DOUBLE TO SHORT  "+res3);
	
	
	byte res4=(byte)a;
	System.out.println("DOUBLE TO BYTE  "+res4);
	
}
}
